#include"so_long.h"

void	find_zerox(char **l, int y, int x)
{
	while (l[y])
	{
		while (l[y][x])
		{
			if (l[y][x] == 'X' && (l[y][x + 1] != '1' && l[y][x + 1] != 'E'))
				l[y][x + 1] = 'X';
			if (l[y][x] == 'X' && (l[y][x - 1] != '1' && l[y][x - 1] != 'E'))
				l[y][x - 1] = 'X';
			if (l[y][x] == 'X' && (l[y + 1][x] != '1' && l[y + 1][x] != 'E'))
				l[y + 1][x] = 'X';
			if (l[y][x] == 'X' && (l[y - 1][x] != '1' && l[y - 1][x] != 'E'))
				l[y - 1][x] = 'X';
			x++;
		}
		x = 0;
		y++;
	}
}
