#include"so_long.h"

void	movement(t_long *s)
{
	s->t++;
	if (s->t == 10)
		flo_pla(s);
	if (s->t == 20)
		flo_pla2(s);
	if (s->t == 30)
		flo_pla3(s);
	if (s->t == 40)
		flo_pla4(s);
	if (s->t == 50)
		flo_pla5(s);
	if (s->t > 50)
		s->t = 0;
}
