#include"so_long.h"

void	moving(t_long *s)
{
	s->r++;
	if (s->r == 10)
		flo_rpla(s);
	if (s->r == 20)
		flo_rpla2(s);
	if (s->r == 30)
		flo_rpla3(s);
	if (s->r == 40)
		flo_rpla4(s);
	if (s->r == 50)
		flo_rpla5(s);
	if (s->r > 50)
		s->r = 0;
}
