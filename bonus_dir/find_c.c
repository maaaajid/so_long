#include"so_long.h"

int	find_c(t_long *s)
{
	int	q;
	int	w;

	w = 0;
	q = 0;
	while (s->lines[w])
	{
		while (s->lines[w][q])
		{
			if (s->lines[w][q] == 'C')
				return (1);
			q++;
		}
		q = 0;
		w++;
	}
	return (0);
}
