#include"ft_printf.h"

int	putstr(char *str)
{
	int	x;

	x = 0;
	if (!str)
	{
		putstr("(null)");
		return (6);
	}
	while (str[x])
	{
		write(1, &str[x], 1);
		x++;
	}
	return (x);
}
